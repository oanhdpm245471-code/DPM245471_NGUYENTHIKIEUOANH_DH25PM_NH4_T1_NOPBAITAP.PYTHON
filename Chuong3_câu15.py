'''
Giải thích cách chạy các dòng lệnh range
-> range(5) sẽ tạo ra một dãy từ 0-4 (5 số, bắt đầu từ số 0)
-> range(5, 10) sẽ tạo một dãy từ 5-9 (5 số, bắt đầu từ số 5)
-> range(5, 20, 3) sẽ tạo một dãy từ 5-19 với bước nhảy là 3 (5, 8, 11...)
->range(20, 5, -1) sẽ tạo ra một dãy từ 20-6 với bước nhảy là -1 (20, 19, 18...)
->range(20, 5, -3) sẽ tạo ra một dãy từ 20-6 với bước nhảy là -3 (20, 17, 14...)
-> range(10, 5) sẽ không tạo ra gì vì bắt đầu lớn hơn kết thúc 
-> range(0) sẽ không tạo ra gì vì không có số nào trong dãy 
-> range(10, 101, 10) sẽ tạo ra một dãy từ 10-100 với bước nhảy là 10 (10, 20, 30...)
-> range(10, -1, -1) sẽ tạo ra một dãy từ 10-0 với bước nhảy là -1 (10, 9, 8...)
-> range(-3, 4) sẽ tạo một dãy từ -3-3 (7 số bắt đầu từ -3)
-> range(0, 10, 1) sẽ tạo ra một dãy từ 0-9 với bước nhảy là 1 (0, 1, 2...)
'''